try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator cannot be zero.")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")
#When will a ValueError occur? A ValueError occurs when the user enters something that can't be converted to an integer using int().
#When will a ZeroDivisionError occur? A ZeroDivisionError occurs when the user enters 0 as the denominator, since division by zero is undefined.